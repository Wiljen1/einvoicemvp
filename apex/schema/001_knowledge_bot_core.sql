join analytics_sources ans on ans.analytics_source_id = cc.analytics_source_id;

create or replace view kb_dashboard_v as
select
  (select count(*) from channels) total_channels,
  (select count(*) from channels where enabled_yn = 'Y') enabled_channels,
  (select count(*) from knowledge_sources) total_knowledge_sources,
  (select count(*) from question_logs) total_questions,
  (select count(*) from analytics_events) total_analytics_events,
  (select count(*) from runners where status = 'ONLINE') online_runners,
  (select count(*) from runners where codex_available_yn = 'Y') codex_available_runners,
  (select max(created_at) from question_logs) latest_question_at,
  (select max(last_heartbeat_at) from runners) latest_runner_heartbeat_at
from dual;

