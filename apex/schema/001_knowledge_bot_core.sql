merge into runners t
using (
  select
    rp.runner_pool_id,
    'local-shared-runner' runner_key,
    'Local Shared Codex Runner' name
  from runner_pools rp
  where rp.pool_key = 'default-codex-runner-pool'
) s
on (t.runner_key = s.runner_key)
when matched then update set
  t.runner_pool_id = s.runner_pool_id,
  t.name = s.name
when not matched then insert (
  runner_pool_id, runner_key, name, runner_type, platform, status, codex_available_yn, metadata_json
) values (
  s.runner_pool_id, s.runner_key, s.name, 'CLI', 'LOCAL', 'OFFLINE', 'N', json_object('seeded' value true)
);

merge into system_settings t
using (
  select 'DEPLOYMENT_MODE' setting_key, 'LOCAL' setting_value, 'STRING' value_type, 'Current deployment runner_mode.' description from dual union all
  select 'BACKEND_MODE', 'APEX', 'STRING', 'Slack runtime should use APEX/ORDS as backend.' from dual union all
  select 'BOT_CONFIG_REFRESH_INTERVAL_HOURS', '24', 'NUMBER', 'Configuration refresh interval.' from dual union all
  select 'ENABLE_MSAL_GRAPH', 'false', 'BOOLEAN', 'Future Microsoft Graph integration flag.' from dual
) s
on (t.setting_key = s.setting_key)
when matched then update set t.setting_value = s.setting_value, t.value_type = s.value_type, t.description = s.description
when not matched then insert (setting_key, setting_value, value_type, description)
values (s.setting_key, s.setting_value, s.value_type, s.description);

commit;

