create or replace view kb_analytics_summary_v as
select
  trunc(cast(created_at as timestamp)) event_day,
  count(*) total_events,
  sum(case when json_value(event_json, '$.cacheHit') = 'true' then 1 else 0 end) cache_hits,
  avg(to_number(nullif(json_value(event_json, '$.responseTimeMs'), ''))) avg_response_time_ms
from analytics_events
group by trunc(cast(created_at as timestamp));

